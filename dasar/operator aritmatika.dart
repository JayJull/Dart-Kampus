void main() {
  var a,b,hasil;
  a = 30;
  b = 20;

  print("Operasi Aritmatika");
  // 1. Pertambahan
  print("\npertambahan");
  hasil =  a + b;
  print("$a + $b = $hasil");

  // 2. Pengurangan
  print("\nPengurangan");
  hasil = a - b;
  print("$a - $b = $hasil");

  // 3. Perkalian
  print("\nPerkalian");
  hasil = a * b;
  print("$a * $b = $hasil");

  // 4. Pembagian
  //pembagian ada 2 macam / untuk hasil double, /~ untuk hasil int
  print("\Pembagian");
  hasil = a / b;
  print("$a / $b = $hasil");
}